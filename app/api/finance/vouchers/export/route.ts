import { NextResponse } from "next/server";
import PDFDocument from "pdfkit";
import * as XLSX from "xlsx";

import { resolvePlatformContext } from "@/lib/api/context";
import { requireSchoolModule } from "@/lib/module-governance";

type Row = Record<string, any>;

const money = (value: unknown) => `₹${Number(value || 0).toLocaleString("en-IN", { minimumFractionDigits: 2, maximumFractionDigits: 2 })}`;
const text = (value: unknown, fallback = "-") => {
  const output = String(value ?? "").trim();
  return output || fallback;
};

function collectPdf(render: (doc: PDFKit.PDFDocument) => void) {
  return new Promise<Buffer>((resolve, reject) => {
    const doc = new PDFDocument({ size: "A4", layout: "portrait", margin: 24, bufferPages: true });
    const chunks: Buffer[] = [];
    doc.on("data", (chunk) => chunks.push(Buffer.from(chunk)));
    doc.on("end", () => resolve(Buffer.concat(chunks)));
    doc.on("error", reject);
    render(doc);
    const range = doc.bufferedPageRange();
    for (let i = range.start; i < range.start + range.count; i += 1) {
      doc.switchToPage(i);
      doc.fillColor("#6B7280").fontSize(8).text(`Generated by TOTTECH ONE • Page ${i + 1} of ${range.count}`, 24, 805, { align: "center", width: 547 });
    }
    doc.end();
  });
}

async function loadData(request: Request) {
  const url = new URL(request.url);
  url.pathname = url.pathname.replace(/\/export$/, "");
  url.searchParams.delete("format");
  const response = await fetch(url.toString(), { headers: request.headers, cache: "no-store" });
  return (await response.json()) as { context?: { selectedSchool?: Row | null; selectedAcademicYear?: Row | null; filters?: Row }; vouchers?: Row[]; summary?: Row };
}

function drawVoucher(doc: PDFKit.PDFDocument, voucher: Row, x: number, y: number, width: number, height: number, schoolName: string, copyLabel: string, logoPath: string | null) {
  doc.save();
  doc.roundedRect(x, y, width, height, 8).lineWidth(1).strokeColor("#1F2937").stroke();
  doc.fillColor("#F9FAFB").rect(x, y, width, 28).fill();
  doc.fillColor("#111827").font("Helvetica-Bold").fontSize(11).text(schoolName, x + 10, y + 8, { width: width - 20, align: "center" });
  if (logoPath) {
    try { doc.image(logoPath, x + 10, y + 36, { width: 44, height: 44 }); } catch {}
  }
  doc.fillColor("#D4AF37").font("Helvetica-Bold").fontSize(10).text(copyLabel, x + width - 110, y + 8, { width: 100, align: "right" });
  doc.fillColor("#111827").font("Helvetica-Bold").fontSize(16).text("EXPENSE VOUCHER", x + 60, y + 40, { width: width - 120, align: "center" });
  doc.fillColor("#6B7280").fontSize(8).text(`Voucher No: ${text(voucher.voucher_number)}   Date: ${text(voucher.voucher_date || voucher.expense_date)}`, x + 60, y + 62, { width: width - 120, align: "center" });

  const rows = [
    ["Paid To", text(voucher.paid_to || voucher.vendor_name)],
    ["Mobile", text(voucher.mobile_number)],
    ["Address", text(voucher.payee_address)],
    ["Category", text(voucher.category).replaceAll("_", " ")],
    ["Purpose", text(voucher.purpose || voucher.description)],
    ["Amount", money(voucher.amount)],
    ["Amount in Words", text(voucher.amount_in_words)],
    ["Payment Mode", text(voucher.payment_method).replaceAll("_", " ")],
    ["Reference No", text(voucher.reference_number)],
    ["Remarks", text(voucher.remarks)],
  ];

  let rowY = y + 90;
  rows.forEach(([label, value]) => {
    doc.fillColor("#6B7280").font("Helvetica-Bold").fontSize(8).text(`${label}:`, x + 12, rowY, { width: 90 });
    doc.fillColor("#111827").font("Helvetica").fontSize(8).text(String(value), x + 102, rowY, { width: width - 122 });
    rowY += 16;
  });

  doc.moveTo(x + 14, y + height - 42).lineTo(x + width - 14, y + height - 42).strokeColor("#9CA3AF").stroke();
  doc.fillColor("#111827").font("Helvetica-Bold").fontSize(8).text("Receiver Signature", x + 14, y + height - 34);
  doc.text("Accountant Signature", x + width / 2 - 50, y + height - 34);
  doc.text("Principal Signature", x + width - 110, y + height - 34);
  doc.restore();
}

export async function GET(request: Request) {
  const guard = await requireSchoolModule("FINANCE");
  if (guard.response) return guard.response;

  const context = await resolvePlatformContext(request);
  if (!context) {
    return NextResponse.json({ error: "Unauthorized" }, { status: 401 });
  }

  const payload = await loadData(request);
  const rows = payload.vouchers || [];
  const format = new URL(request.url).searchParams?.get("format") === "xlsx" ? "xlsx" : "pdf";

  if (format === "xlsx") {
    const workbook = XLSX.utils.book_new();
    XLSX.utils.book_append_sheet(
      workbook,
      XLSX.utils.json_to_sheet(rows.map((row) => ({
        "Voucher No": row.voucher_number,
        "Voucher Date": row.voucher_date || row.expense_date,
        "Paid To": row.paid_to || row.vendor_name,
        "Category": row.category,
        "Amount": row.amount,
        "Status": row.voucher_status || row.status,
        "Purpose": row.purpose || row.description,
      }))),
      "Vouchers"
    );
    const summary = payload.summary || {};
    XLSX.utils.book_append_sheet(
      workbook,
      XLSX.utils.json_to_sheet([{ "Voucher Count": summary.voucherCount || 0, "Total Expense": summary.totalExpense || 0, "Paid Expense": summary.paidExpense || 0, "Pending Expense": summary.pendingExpense || 0 }]),
      "Summary"
    );
    const buffer = XLSX.write(workbook, { type: "buffer", bookType: "xlsx" }) as Buffer;
    return new NextResponse(new Uint8Array(buffer), {
      headers: {
        "Content-Type": "application/vnd.openxmlformats-officedocument.spreadsheetml.sheet",
        "Content-Disposition": `attachment; filename="expense-vouchers.xlsx"`,
      },
    });
  }

  const schoolName = payload.context?.selectedSchool?.school_name || "TOTTECH ONE";
  const academicYear = payload.context?.selectedAcademicYear?.academic_year || "All Years";
  const logoUrl = payload.context?.selectedSchool?.logo_url ? String(payload.context?.selectedSchool?.logo_url) : null;
  const logoPath = logoUrl && logoUrl.startsWith("/uploads") ? `${process.cwd()}/public${logoUrl}` : null;

  const pdf = await collectPdf((doc) => {
    const pageWidth = 547;
    const voucherHeight = 360;
    let y = 18;
    rows.slice(0, 20).forEach((voucher, index) => {
      if (index % 2 === 0 && index > 0) {
        doc.addPage();
        y = 18;
      }
      drawVoucher(doc, voucher, 24, y, pageWidth, voucherHeight, schoolName, index % 2 === 0 ? "RECEIVER COPY" : "SCHOOL RECORD COPY", logoPath);
      if (index % 2 === 0) {
        doc.moveTo(24, y + voucherHeight + 8).lineTo(571, y + voucherHeight + 8).dash(4, { space: 2 }).strokeColor("#9CA3AF").stroke();
        doc.undash();
      }
      y += voucherHeight + 20;
    });
    doc.fillColor("#6B7280").fontSize(8).text(`School/College: ${schoolName} • Academic Year: ${academicYear}`, 24, 770, { width: 547, align: "center" });
  });

  return new NextResponse(new Uint8Array(pdf), {
    headers: {
      "Content-Type": "application/pdf",
      "Content-Disposition": `attachment; filename="expense-vouchers.pdf"`,
    },
  });
}
