import fs from "fs/promises";
import path from "path";

import PDFDocument from "pdfkit";

type SchoolBranding = {
  school_name?: string | null;
  school_code?: string | null;
  school_logo?: string | null;
  logo_url?: string | null;
  address?: string | null;
  phone?: string | null;
  email?: string | null;
  recognition_number?: string | null;
  affiliation_number?: string | null;
  recognition_authority?: string | null;
  affiliation_authority?: string | null;
};

type StudentRecord = {
  id?: number;
  first_name?: string | null;
  middle_name?: string | null;
  last_name?: string | null;
  name?: string | null;
  admission_number?: string | null;
  enrollment_number?: string | null;
  father_name?: string | null;
  mother_name?: string | null;
  dob?: string | Date | null;
  current_class_id?: number | null;
  current_section_id?: number | null;
  school_id?: number | null;
  academic_year_id?: number | null;
  roll_number?: string | null;
  student_status?: string | null;
};

type CertificateRenderInput = {
  documentNumber: string;
  title: string;
  subtitle: string;
  school: SchoolBranding;
  student: StudentRecord;
  academicYear?: string | null;
  className?: string | null;
  sectionName?: string | null;
  issueDate?: Date | null;
  dateOfJoining?: string | Date | null;
  dateOfLeaving?: string | Date | null;
  reasonForLeaving?: string | null;
  conduct?: string | null;
  attendance?: string | null;
  statusLabel?: string | null;
  narrative: string;
  extraRows?: Array<[string, unknown]>;
};

const pageWidth = 595.28;
const margin = 42;

const logoFileFromUrl = (logoUrl: string | null | undefined) => {
  if (!logoUrl || logoUrl.startsWith("http")) {
    return null;
  }

  const normalized = logoUrl.startsWith("/")
    ? logoUrl.slice(1)
    : logoUrl;
  const candidate = path.join(
    process.cwd(),
    "public",
    normalized
  );

  return candidate;
};

const textValue = (value: unknown) => {
  if (
    value === null ||
    value === undefined ||
    value === ""
  ) {
    return "-";
  }

  if (value instanceof Date) {
    return value
      .toISOString()
      .slice(0, 10);
  }

  const date = new Date(String(value));
  if (!Number.isNaN(date.getTime())) {
    return date
      .toISOString()
      .slice(0, 10);
  }

  return String(value);
};

const money = (value: unknown) => {
  const parsed = Number(value);
  if (!Number.isFinite(parsed)) {
    return "-";
  }
  return `Rs. ${parsed.toFixed(2)}`;
};

const drawFooter = (doc: PDFKit.PDFDocument, documentNumber: string) => {
  const pageNumber = doc.bufferedPageRange().count;
  const current = 1;

  doc
    .moveTo(margin, 760)
    .lineTo(pageWidth - margin, 760)
    .strokeColor("#CBD5E1")
    .stroke();
  doc
    .fillColor("#64748B")
    .font("Helvetica")
    .fontSize(8)
    .text(
      `Generated by TOTTECH ONE | Document ${documentNumber} | Page ${current} of ${pageNumber}`,
      margin,
      768,
      {
        width: pageWidth - margin * 2,
        align: "center",
      }
    );
};

export async function renderStudentCertificatePdf(
  input: CertificateRenderInput
) {
  const doc = new PDFDocument({
    size: "A4",
    margin: 0,
    bufferPages: true,
  });

  const chunks: Buffer[] = [];
  doc.on("data", (chunk) =>
    chunks.push(Buffer.from(chunk))
  );

  const logoPath = logoFileFromUrl(
    input.school.school_logo ||
      input.school.logo_url ||
      null
  );

  doc
    .rect(0, 0, pageWidth, 150)
    .fill("#04142E");

  if (logoPath) {
    try {
      await fs.access(logoPath);
      doc.image(logoPath, margin, 20, {
        fit: [62, 62],
        align: "center",
        valign: "center",
      });
    } catch {
      doc
        .roundedRect(margin, 20, 62, 62, 10)
        .strokeColor("#D4AF37")
        .stroke();
      doc
        .fillColor("#D4AF37")
        .font("Helvetica-Bold")
        .fontSize(12)
        .text("TT", margin, 44, {
          width: 62,
          align: "center",
        });
    }
  } else {
    doc
      .roundedRect(margin, 20, 62, 62, 10)
      .strokeColor("#D4AF37")
      .stroke();
    doc
      .fillColor("#D4AF37")
      .font("Helvetica-Bold")
      .fontSize(12)
      .text("TT", margin, 44, {
        width: 62,
        align: "center",
      });
  }

  doc
    .fillColor("#D4AF37")
    .font("Helvetica-Bold")
    .fontSize(9)
    .text(
      input.school.school_code ||
        "SCHOOL",
      118,
      12,
      { width: pageWidth - 160, align: "center" }
    );
  doc
    .fillColor("#FFFFFF")
    .font("Helvetica-Bold")
    .fontSize(17)
    .text(
      input.school.school_name ||
        "School/College",
      118,
      28,
      { width: pageWidth - 160, align: "center" }
    );
  doc
    .fillColor("#E5EEF8")
    .font("Helvetica")
    .fontSize(8.3)
    .text(
      input.school.address || "",
      118,
      52,
      {
        width: pageWidth - 160,
        lineGap: 1.5,
        align: "center",
      }
    );
  doc
    .fillColor("#E5EEF8")
    .font("Helvetica")
    .fontSize(8.1)
    .text(
      input.school.phone ? `Phone: ${input.school.phone}` : "",
      118,
      68,
      {
        width: pageWidth - 160,
        align: "center",
      }
    );
  doc
    .fillColor("#E5EEF8")
    .font("Helvetica")
    .fontSize(8.1)
    .text(
      input.school.email ? `Email: ${input.school.email}` : "",
      118,
      80,
      {
        width: pageWidth - 160,
        align: "center",
      }
    );
  doc
    .fillColor("#D4AF37")
    .font("Helvetica-Bold")
    .fontSize(10)
    .text(input.subtitle, 118, 102, {
      width: pageWidth - 160,
      align: "center",
    });

  doc
    .fillColor("#0F172A")
    .font("Helvetica-Bold")
    .fontSize(18)
    .text(input.title, margin, 154, {
      width: pageWidth - margin * 2,
      align: "center",
    });

  doc
    .fillColor("#334155")
    .font("Helvetica")
    .fontSize(10.5)
    .text(
      input.narrative,
      margin + 14,
      190,
      {
        width: pageWidth - margin * 2 - 28,
        align: "justify",
        lineGap: 4,
      }
    );

  const rowPairs: Array<[string, unknown]> = [
    ["Document Number", input.documentNumber],
    ["Student Name", studentName(input.student)],
    ["Admission Number", input.student.admission_number],
    ["Father Name", input.student.father_name],
    ["Mother Name", input.student.mother_name],
    ["Date of Birth", input.student.dob],
    ["Academic Year", input.academicYear],
    ["Class", input.className],
    ["Section", input.sectionName],
    ["Date of Joining", input.dateOfJoining],
    ["Date of Leaving", input.dateOfLeaving],
    ["Reason For Leaving", input.reasonForLeaving],
    ["Conduct", input.conduct],
    ["Attendance", input.attendance],
    ["Student Status", input.statusLabel],
  ];

  if (input.extraRows?.length) {
    rowPairs.push(...input.extraRows);
  }

  const startY = 270;
  const boxHeight = 28;
  const leftX = margin;
  const rightX = 305;
  const boxWidth = 248;
  const rowsPerColumn = Math.ceil(
    rowPairs.length / 2
  );

  const drawBox = (
    x: number,
    y: number,
    label: string,
    value: unknown
  ) => {
    doc
      .roundedRect(x, y, boxWidth, boxHeight, 8)
      .strokeColor("#CBD5E1")
      .lineWidth(0.9)
      .stroke();
    doc
      .fillColor("#64748B")
      .font("Helvetica-Bold")
      .fontSize(7.6)
      .text(label.toUpperCase(), x + 9, y + 4, {
        width: boxWidth - 18,
      });
    doc
      .fillColor("#0F172A")
      .font("Helvetica-Bold")
      .fontSize(9)
      .text(textValue(value), x + 9, y + 13, {
        width: boxWidth - 18,
      });
  };

  rowPairs
    .slice(0, rowsPerColumn)
    .forEach(([label, value], index) => {
      drawBox(
        leftX,
        startY + index * 34,
        label,
        value
      );
    });

  rowPairs
    .slice(rowsPerColumn)
    .forEach(([label, value], index) => {
      drawBox(
        rightX,
        startY + index * 34,
        label,
        value
      );
    });

  const signatureY = 560;
  const signatureAreaHeight = 80;
  doc
    .roundedRect(
      margin,
      signatureY,
      pageWidth - margin * 2,
      signatureAreaHeight,
      10
    )
    .strokeColor("#CBD5E1")
    .lineWidth(0.9)
    .stroke();
  doc
    .moveTo(margin + 18, signatureY + 42)
    .lineTo(margin + 220, signatureY + 42)
    .strokeColor("#94A3B8")
    .stroke();
  doc
    .moveTo(pageWidth - margin - 180, signatureY + 42)
    .lineTo(pageWidth - margin - 18, signatureY + 42)
    .strokeColor("#94A3B8")
    .stroke();
  doc
    .fillColor("#0F172A")
    .font("Helvetica-Bold")
    .fontSize(10)
    .text("Principal Signature", margin + 18, signatureY + 16, {
      width: 200,
      align: "left",
    });
  doc
    .fillColor("#0F172A")
    .font("Helvetica-Bold")
    .fontSize(10)
    .text("Date", pageWidth - margin - 70, signatureY + 16, {
      width: 52,
      align: "left",
    });
  doc
    .fillColor("#64748B")
    .font("Helvetica")
    .fontSize(8)
    .text(
      "Leave this area blank for manual sign-off and stamping.",
      margin + 18,
      signatureY + 54,
      {
        width: pageWidth - margin * 2 - 36,
        align: "left",
      }
    );

  doc
    .roundedRect(margin, 652, pageWidth - margin * 2, 58, 12)
    .strokeColor("#D4AF37")
    .lineWidth(1.1)
    .stroke();
  doc
    .fillColor("#04142E")
    .font("Helvetica-Bold")
    .fontSize(11)
    .text(
      `Generated by TOTTECH ONE | ${input.documentNumber}`,
      margin + 14,
      666,
      {
        width: pageWidth - margin * 2 - 28,
        align: "center",
      }
    );
  doc
    .fillColor("#475569")
    .font("Helvetica")
    .fontSize(8.5)
    .text(
      "This certificate copy is stored in the student document vault and can be shared through public PDF download links.",
      margin + 18,
      682,
      {
        width: pageWidth - margin * 2 - 36,
        align: "center",
      }
    );

  drawFooter(doc, input.documentNumber);
  doc.end();

  return new Promise<Buffer>((resolve, reject) => {
    doc.on("end", () =>
      resolve(Buffer.concat(chunks))
    );
    doc.on("error", reject);
  });
}

function studentName(student: StudentRecord) {
  return (
    [student.first_name, student.middle_name, student.last_name]
      .filter(Boolean)
      .join(" ") ||
    student.name ||
    "-"
  );
}

export function studentCertificateFileName(
  documentNumber: string,
  type: "TRANSFER_CERTIFICATE" | "STUDY_CERTIFICATE"
) {
  const suffix =
    type === "TRANSFER_CERTIFICATE"
      ? "transfer-certificate"
      : "study-certificate";

  return `${suffix}-${String(documentNumber).replace(/[^a-zA-Z0-9_-]/g, "-")}.pdf`;
}

export function studentCertificateRelativePath(
  fileName: string
) {
  return `/uploads/students/certificates/${fileName}`;
}
