type ThemeColors = (typeof palette)[ExecutiveTheme];

const text = (value: unknown, fallback = "-") => {
  const output = String(value ?? "").trim();
  return output || fallback;
};

const money = (value: unknown, prefix = "₹") => `${prefix}${Number(value || 0).toLocaleString("en-IN", { minimumFractionDigits: 2, maximumFractionDigits: 2 })}`;

const number = (value: unknown) => Number(value || 0) || 0;

const safeDate = (value: unknown) => {
  const date = value instanceof Date ? value : new Date(String(value || new Date()));
  return Number.isNaN(date.getTime()) ? new Date() : date;
};

const formatDate = (value: unknown) => safeDate(value).toLocaleDateString("en-IN", { day: "2-digit", month: "short", year: "numeric" });

function logoPath(logoUrl?: string | null) {
  const url = String(logoUrl || "").trim();
  if (!url.startsWith("/")) return null;
  const file = path.join(process.cwd(), "public", url.replace(/^\/+/, ""));
  return fs.existsSync(file) ? file : null;
}

function collectPdf(render: (doc: PDFKit.PDFDocument) => void | Promise<void>, landscape = true) {
  return new Promise<Buffer>((resolve, reject) => {
    const doc = new PDFDocument({ size: landscape ? "A4" : "A4", layout: landscape ? "landscape" : "portrait", margin: 0, autoFirstPage: true, bufferPages: true });
    const chunks: Buffer[] = [];
    doc.on("data", (chunk) => chunks.push(Buffer.from(chunk)));
    doc.on("end", () => resolve(Buffer.concat(chunks)));
    doc.on("error", reject);
    Promise.resolve(render(doc)).then(() => {
      const range = doc.bufferedPageRange();
      for (let i = range.start; i < range.start + range.count; i += 1) {
        doc.switchToPage(i);
        doc.fillColor("#6B7280").fontSize(8).text(`Generated by TOTTECH ONE • Page ${i + 1} of ${range.count}`, 28, 548, { align: "center", width: 786 });
      }
      doc.end();
    }).catch(reject);
  });
}

function drawHeader(doc: PDFKit.PDFDocument, input: ExecutivePdfInput, colors: ThemeColors) {
  const img = logoPath(input.logoUrl);
  doc.save();
  doc.rect(0, 0, 842, 90).fill(colors.navy);
  doc.restore();
  if (img) {
    try { doc.image(img, 24, 18, { width: 52, height: 52 }); } catch {}
  }
  doc.fillColor("#FFFFFF").font("Helvetica-Bold").fontSize(20).text(text(input.schoolName, "TOTTECH ONE"), img ? 88 : 28, 18, { width: 500 });
  doc.fillColor(colors.gold).fontSize(10).text(input.title, img ? 88 : 28, 44);
  doc.fillColor("#E5E7EB").fontSize(8).text(`Academic Year: ${text(input.academicYear, "All Years")}  •  Generated: ${formatDate(input.generatedAt)}`, img ? 88 : 28, 60, { width: 700 });
  const filterText = Object.entries(input.filters || {}).filter(([, value]) => value !== null && value !== undefined && String(value).trim() !== "").map(([key, value]) => `${key.replaceAll("_", " ")}: ${String(value)}`).join("  •  ");
  if (filterText) {
    doc.fillColor("#CBD5E1").fontSize(7).text(filterText, 28, 76, { width: 786 });
  }
}

function drawKpis(doc: PDFKit.PDFDocument, kpis: ExecutivePdfInput["kpis"], colors: ThemeColors, y = 108) {
  const cards = kpis || [];
  const cardWidth = 786 / Math.max(cards.length, 1);
  cards.forEach((card, index) => {
    const x = 28 + index * cardWidth;
    doc.roundedRect(x, y, cardWidth - 8, 66, 8).fillAndStroke(colors.card, colors.border);
    doc.fillColor(colors.muted).font("Helvetica-Bold").fontSize(8).text(card.label.toUpperCase(), x + 10, y + 10, { width: cardWidth - 28 });
    doc.fillColor(colors.ink).font("Helvetica-Bold").fontSize(16).text(card.value, x + 10, y + 30, { width: cardWidth - 28 });
  });
}

function drawChartBox(doc: PDFKit.PDFDocument, title: string, x: number, y: number, w: number, h: number, colors: ThemeColors) {
  doc.roundedRect(x, y, w, h, 8).fillAndStroke(colors.card, colors.border);
  doc.fillColor(colors.ink).font("Helvetica-Bold").fontSize(10).text(title, x + 10, y + 10);
}

function drawBarChart(doc: PDFKit.PDFDocument, rows: NonNullable<ExecutivePdfInput["trendSeries"]>, x: number, y: number, w: number, h: number, colors: ThemeColors) {
  drawChartBox(doc, "Monthly Trend", x, y, w, h, colors);
  const values = rows.map((r) => number(r.value));
  const max = Math.max(1, ...values);
  const innerX = x + 12;
  const innerY = y + 28;
  const innerH = h - 42;
  const barGap = 8;
  const barW = Math.max(16, (w - 24 - (rows.length - 1) * barGap) / Math.max(rows.length, 1));
  rows.slice(0, 8).forEach((row, index) => {
    const bh = Math.max(8, (number(row.value) / max) * (innerH - 16));
    const bx = innerX + index * (barW + barGap);
    const by = innerY + innerH - bh;
    const color = row.color || (index % 2 === 0 ? colors.gold : colors.blue);
    doc.roundedRect(bx, by, barW, bh, 4).fill(color);
    doc.fillColor(colors.ink).fontSize(7).font("Helvetica-Bold").text(text(row.label, "-"), bx, innerY + innerH + 2, { width: barW, align: "center" });
    doc.fillColor(colors.muted).fontSize(7).text(number(row.value).toLocaleString("en-IN"), bx, by - 10, { width: barW, align: "center" });
  });
}

function drawPie(doc: PDFKit.PDFDocument, rows: NonNullable<ExecutivePdfInput["pieSeries"]>, x: number, y: number, size: number, title: string, colors: ThemeColors) {
  drawChartBox(doc, title, x, y, size, size + 18, colors);
  const total = rows.reduce((sum, row) => sum + Math.max(0, number(row.value)), 0) || 1;
  let start = -Math.PI / 2;
  const cx = x + size * 0.42;
  const cy = y + size * 0.60;
  const radius = Math.min(size * 0.28, 78);
  rows.slice(0, 6).forEach((row, index) => {
    const fraction = Math.max(0, number(row.value)) / total;
    const end = start + fraction * Math.PI * 2;
    const color = row.color || [colors.gold, colors.blue, colors.green, colors.orange, colors.purple, colors.teal][index % 6];
    doc.save();
    doc.moveTo(cx, cy).lineTo(cx + Math.cos(start) * radius, cy + Math.sin(start) * radius).path(`M ${cx} ${cy} A ${radius} ${radius} 0 ${(end - start) > Math.PI ? 1 : 0} 1 ${cx + Math.cos(end) * radius} ${cy + Math.sin(end) * radius}`).closePath().fill(color);
    doc.restore();
    start = end;
  });
  rows.slice(0, 6).forEach((row, index) => {
    doc.fillColor(colors.ink).fontSize(7).text(`${text(row.label, '-')}: ${number(row.value).toLocaleString('en-IN')}`, x + size * 0.68, y + 30 + index * 12, { width: size * 0.27 });
  });
}

function drawRanking(doc: PDFKit.PDFDocument, title: string, rows: NonNullable<ExecutivePdfInput["topPerformers"]>, x: number, y: number, w: number, h: number, colors: ThemeColors) {
  drawChartBox(doc, title, x, y, w, h, colors);
  rows.slice(0, 5).forEach((row, index) => {
    const yy = y + 28 + index * 22;
    doc.fillColor(colors.muted).fontSize(8).text(`${index + 1}.`, x + 10, yy, { width: 12 });
    doc.fillColor(colors.ink).font("Helvetica-Bold").fontSize(8).text(text(row.label, '-'), x + 24, yy, { width: w - 110 });
    doc.fillColor(colors.gold).font("Helvetica-Bold").fontSize(8).text(String(row.value), x + w - 70, yy, { width: 56, align: 'right' });
    if (row.meta) {
      doc.fillColor(colors.muted).font("Helvetica").fontSize(7).text(text(row.meta), x + 24, yy + 10, { width: w - 40 });
    }
  });
}

function drawInsightPanel(doc: PDFKit.PDFDocument, title: string, rows: string[], x: number, y: number, w: number, h: number, colors: ThemeColors, accent: string = colors.gold) {
  drawChartBox(doc, title, x, y, w, h, colors);
  rows.slice(0, 6).forEach((row, index) => {
    const yy = y + 30 + index * 18;
    doc.fillColor(accent).font("Helvetica-Bold").fontSize(8).text("•", x + 10, yy);
    doc.fillColor(colors.ink).font("Helvetica").fontSize(8).text(row, x + 20, yy, { width: w - 30 });
  });
}

function rowsToInsights(rows: Row[], type: string, kpis: Record<string, unknown>) {
  const generated = number(kpis.totalRevenue);
  const collected = number(kpis.collectedAmount);
  const pending = number(kpis.pendingAmount);
  const collection = generated > 0 ? Math.round((collected / generated) * 100) : 0;
  const top = rows.slice(0, 3).map((row) => `${text(row.label || row.student_name || row.class_name || row.invoice_number || row.category_name, 'Record')} at ${text(row.value || row.total_amount || row.amount || row.balance_amount, '-')}`);
  return [
    `${type} report generated from live school records.`,
    `Collection efficiency is ${collection}% with pending exposure of ${money(pending)}.`,
    `Total invoiced revenue is ${money(generated)} and collected amount is ${money(collected)}.`,
    ...top,
  ];
}

export async function renderExecutiveFinanceReportPdf(report: {
  type: string;
  title: string;
  context?: any;
  kpis?: Record<string, unknown>;
  rows: Row[];
  totals: Row;
  topPerformers?: Array<{ label: string; value: number; meta?: string }>;
  bottomPerformers?: Array<{ label: string; value: number; meta?: string }>;
  trendSeries?: Array<{ label: string; value: number; color?: string }>;
  pieSeries?: Array<{ label: string; value: number; color?: string }>;
  pageFooter?: string;
  theme?: ExecutiveTheme;
  aiInsights?: string[];
  aiForecasting?: string[];
}, theme: ExecutiveTheme = "color") {
  const colors = palette[theme];
  const rows = report.rows || [];
  const kpis = report.kpis || {};
  const school = report.context?.selectedSchool || null;
  const academicYear = report.context?.selectedAcademicYear || null;
  const monthly = (report.trendSeries?.length ? report.trendSeries : rows.map((row, index) => ({ label: text(row.month || row.label || row.invoice_number || `Item ${index + 1}`), value: number(row.collected ?? row.generated ?? row.amount ?? row.pending_amount ?? row.balance_amount), color: index % 2 === 0 ? colors.gold : colors.blue }))).map((row, index) => ({ label: text(row.label), value: number(row.value), color: row.color || (index % 2 === 0 ? colors.gold : colors.blue) }));
  const pie = (report.pieSeries?.length ? report.pieSeries : rows.map((row, index) => ({ label: text(row.label || row.category_name || row.class_name || row.section_name || `Slice ${index + 1}`), value: number(row.collected ?? row.generated ?? row.amount ?? row.pending_amount ?? row.balance_amount), color: index % 2 === 0 ? colors.gold : colors.blue }))).map((row, index) => ({ label: text(row.label), value: number(row.value), color: row.color || (index % 2 === 0 ? colors.gold : colors.blue) }));

  return collectPdf((doc) => {
    drawHeader(doc, {
      title: report.title,
      schoolName: text(school?.school_name, report.context?.all_schools ? "All Schools/Colleges" : "TOTTECH ONE"),
      academicYear: text(academicYear?.academic_year, report.context?.all_years ? "All Years" : "-"),
      generatedAt: report.context?.generatedAt || new Date(),
      logoUrl: school?.logo_url,
      filters: report.context?.filters,
      theme,
    }, colors);

    const kpiCards = [
      { label: "Total Revenue", value: money(kpis.totalRevenue), accent: colors.gold },
      { label: "Total Invoices", value: Number(kpis.totalInvoices || 0).toLocaleString("en-IN"), accent: colors.blue },
      { label: "Collected", value: money(kpis.collectedAmount), accent: colors.green },
      { label: "Pending", value: money(kpis.pendingAmount), accent: colors.orange },
      { label: "Defaulters", value: Number(kpis.defaulters || 0).toLocaleString("en-IN"), accent: colors.bad },
      { label: "Collection %", value: `${Number(kpis.collectionPercentage || 0)}%`, accent: colors.purple },
    ];
    drawKpis(doc, kpiCards, colors, 108);

    drawBarChart(doc, monthly, 28, 190, 250, 180, colors);
    drawPie(doc, pie, 300, 190, 250, report.title.includes("Expense") ? "Expense Distribution" : "Revenue Distribution", colors);
    drawRanking(doc, "Top Performers", (report.topPerformers || rows.map((row) => ({ label: text(row.class_name || row.student_name || row.invoice_number || row.label), value: number(row.generated ?? row.collected ?? row.amount ?? row.pending_amount ?? row.balance_amount), meta: text(row.section_name || row.payment_method || row.status || row.month) }))).slice(0, 5), 564, 190, 250, 180, colors);

    drawRanking(doc, "Lowest Performers / Defaulters", (report.bottomPerformers || rows.slice().reverse().map((row) => ({ label: text(row.class_name || row.student_name || row.invoice_number || row.label), value: number(row.pending_amount ?? row.balance_amount ?? row.pending ?? row.amount ?? row.total_amount), meta: text(row.section_name || row.payment_method || row.status || row.month) }))).slice(0, 5), 28, 388, 250, 150, colors);
    drawInsightPanel(doc, "AI Insights", rowsToInsights(rows, report.type, kpis), 300, 388, 250, 150, colors, colors.gold);
    drawInsightPanel(doc, "AI Forecasting", [
      `Forecast collection risk: ${Math.max(0, 100 - Number(kpis.collectionPercentage || 0))}%`,
      `Budget utilization driven by current expense and pending exposure.`,
      `Monthly trend indicates ${monthly.length} active periods analyzed.`,
      `High-risk defaulters and low-performing classes should be reviewed weekly.`,
    ], 564, 388, 250, 150, colors, colors.goldSoft);

    const bottomY = 556;
    doc.fillColor(colors.navy).font("Helvetica-Bold").fontSize(10).text(`School/College Filters: ${Object.entries(report.context?.filters || {}).filter(([, value]) => value !== null && value !== undefined && String(value).trim() !== "").map(([key, value]) => `${key.replaceAll('_', ' ')}=${String(value)}`).join(" • ") || 'None'}`, 28, bottomY, { width: 786 });
    if (report.pageFooter) {
      doc.fillColor(colors.muted).fontSize(8).text(report.pageFooter, 28, 570, { width: 786, align: 'center' });
    }
  }, true);
}

export async function renderExecutiveExpensePdf(input: {
  schoolName?: string | null;
  academicYear?: string | null;
  generatedAt?: string | Date | null;
  logoUrl?: string | null;
  filters?: Record<string, unknown>;
  summary?: Record<string, unknown>;
  analytics?: { expenseByMonth?: Row[]; expenseByCategory?: Row[]; expenseBySchool?: Row[]; expenseByAcademicYear?: Row[]; expenseByClass?: Row[]; expenseBySection?: Row[]; topCategories?: Row[] };
  expenses?: Row[];
  finance?: Record<string, unknown>;
  theme?: ExecutiveTheme;
}) {
  const colors = palette[input.theme || "color"];
  const summary = input.summary || {};
  const analytics = input.analytics || {};
  const expenses = input.expenses || [];
  const monthly = analytics.expenseByMonth || expenses.map((row, index) => ({ label: text(row.month || row.expense_date || `Item ${index + 1}`), value: number(row.amount || row.total_amount), color: index % 2 === 0 ? colors.gold : colors.orange }));
  const categoryPie = analytics.expenseByCategory || expenses.map((row, index) => ({ label: text(row.category_name || row.category || `Category ${index + 1}`), value: number(row.amount || row.total_amount), color: index % 2 === 0 ? colors.gold : colors.blue }));
  const finance = input.finance || {};

  return collectPdf((doc) => {
    drawHeader(doc, {
      title: "Expense Dashboard PDF",
      schoolName: input.schoolName,
      academicYear: input.academicYear,
      generatedAt: input.generatedAt,
      logoUrl: input.logoUrl,
      filters: input.filters,
    }, colors);

    const kpis = [
      { label: "Total Expense", value: money(summary.totalExpense), accent: colors.bad },
      { label: "Monthly Expense", value: money(summary.monthlyExpense), accent: colors.orange },
      { label: "Yearly Expense", value: money(summary.yearlyExpense), accent: colors.gold },
      { label: "Pending", value: Number(summary.pendingApproval || 0).toLocaleString("en-IN"), accent: colors.warn },
      { label: "Approved", value: Number(summary.approvedExpense || 0).toLocaleString("en-IN"), accent: colors.green },
      { label: "Profit/Loss", value: money((Number(finance.totalRevenue || finance.collectedAmount || 0) - Number(summary.totalExpense || 0))), accent: colors.blue },
    ];
    drawKpis(doc, kpis, colors, 108);

    drawBarChart(doc, monthly.map((row, index) => ({ label: text(row.label), value: number(row.value), color: String(row.color || (index % 2 === 0 ? colors.gold : colors.orange)) })), 28, 190, 250, 180, colors);
    drawPie(doc, categoryPie.map((row, index) => ({ label: text(row.label), value: number(row.value), color: String(row.color || (index % 2 === 0 ? colors.gold : colors.blue)) })), 300, 190, 250, "Expense Distribution", colors);
    drawRanking(doc, "Top Expense Categories", ((analytics.topCategories || categoryPie) as any[]).map((row) => ({ label: text(row.label || row.category_name || row.category), value: number(row.value || row.amount), meta: text(row.meta || row.school_name || row.payment_method) })).slice(0, 5), 564, 190, 250, 180, colors);

    drawRanking(doc, "Budget Utilization", ((analytics.expenseBySchool || []) as any[]).map((row) => ({ label: text(row.label || row.school_name), value: number(row.value || row.amount), meta: text(row.budget || row.utilization || row.pending) })).slice(0, 5), 28, 388, 250, 150, colors);
    drawInsightPanel(doc, "AI Insights", [
      `Expense trend spans ${monthly.length} periods with ${Number(summary.expenseCount || expenses.length || 0)} recorded items.`,
      `Pending approval count is ${Number(summary.pendingApproval || 0)} and approved spend is ${money(summary.approvedExpense)}.`,
      `The largest expense categories should be reviewed against budget thresholds.`,
      `Expense risk is highest where monthly burn exceeds expected fee inflow.`,
    ], 300, 388, 250, 150, colors, colors.gold);
    drawInsightPanel(doc, "AI Forecasting", [
      `Projected next-period spend should be reviewed against fee collection trend.`,
      `Budget utilization should be monitored where category growth exceeds month-over-month averages.`,
      `High-risk categories may require approval controls or vendor renegotiation.`,
    ], 564, 388, 250, 150, colors, colors.goldSoft);

    doc.fillColor(colors.navy).font("Helvetica-Bold").fontSize(10).text(`School/College Filters: ${Object.entries(input.filters || {}).filter(([, value]) => value !== null && value !== undefined && String(value).trim() !== "").map(([key, value]) => `${key.replaceAll('_', ' ')}=${String(value)}`).join(" • ") || 'None'}`, 28, 556, { width: 786 });
  }, true);
}
