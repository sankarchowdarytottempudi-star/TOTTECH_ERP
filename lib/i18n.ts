export function isSupportedLanguage(
  value: string | null | undefined
): value is SupportedLanguage {
  return Boolean(
    value &&
      supportedLanguages.includes(
        value as SupportedLanguage
      )
  );
}

export function normalizeLanguage(
  value: string | null | undefined
): SupportedLanguage {
  const normalized = String(value || "")
    .trim()
    .toLowerCase();

  return isSupportedLanguage(normalized)
    ? normalized
    : defaultLanguage;
}

export function getLanguageLabel(
  language: string | null | undefined
) {
  return languageLabels[
    normalizeLanguage(language)
  ];
}

export function translate(
  language: string | null | undefined,
  key: string,
  fallback?: string
) {
  const lang = normalizeLanguage(language);

  return (
    translationTable[lang]?.[key] ??
    translationTable.en[key] ??
    fallback ??
    key
  );
}

export function translateLabel(
  language: string | null | undefined,
  value: string,
  fallback?: string
) {
  return translate(language, value, fallback);
}

export function extractLanguageFromUserCookie(
  rawValue: string | null | undefined
) {
  if (!rawValue) {
    return null;
  }

  try {
    const parsed = JSON.parse(rawValue) as {
      preferred_language?: string;
      language?: string;
    };

    const candidate =
      parsed.preferred_language ||
      parsed.language;

    return isSupportedLanguage(candidate)
      ? candidate
      : null;
  } catch {
    return null;
  }
}

export function languageOptions() {
  return supportedLanguages.map((code) => ({
    value: code,
    label: getLanguageLabel(code),
  }));
}
